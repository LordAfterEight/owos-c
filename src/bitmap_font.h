const uint8_t* get_bitmap(char character) {
    if (character >= 'A' && character <= 'Z') {
        return FONT_8X8[character - 'A' + 33];
    }
    if (character >= 'a' && character <= 'z') {
        return FONT_8X8[character - 'a' + 65];
    }
    if (character >= 32 && character <= 126) {
        return FONT_8X8[character - 32];
    }
    return FONT_8X8[0]; // fallback space
}
